package sample;

public class TotalCost {
    private double km, cost;

    public TotalCost(double km, double cost) {
        this.km = km;
        this.cost = cost;
    }

    public double getKm() {
        return km;
    }

    public void setKm(double km) {
        this.km = km;
    }

    public double getCost() {
        return cost;
    }

    public void setCost(double cost) {
        this.cost = cost;
    }
}
